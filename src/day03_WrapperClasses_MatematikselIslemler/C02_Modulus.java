package day03_WrapperClasses_MatematikselIslemler;

public class C02_Modulus {
    public static void main(String[] args) {

        System.out.println(85%6);// 1

        /*
        modulus (%):
        bir sayının birler basamagını bulmada
        3,5,15... gibi herhangi bir sayının katı olup olmadıgını
        çift sayı olup olmadıgı
        ANLAMAK İÇİN KULLANILIR!

         */
        System.out.println(85% 6);//1
        System.out.println(85/6);//14

        System.out.println(283%2);// tek mi çift mi?  1==> tek sayidir.

        System.out.println(347%5);// 347 sayısı 5 ile tam bölünür mü?   2==>tam bölünemez.


    }
}
