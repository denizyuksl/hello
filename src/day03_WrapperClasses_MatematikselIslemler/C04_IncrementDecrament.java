public class
C04_IncrementDecrament {
    public static void main(String[] args) {
        int sayi = 20;
        sayi = sayi + 3;
        System.out.println(sayi);// 23

        // sayıyı 5 arttırıp yazdırın.
        System.out.println(sayi += 5);

        // sayının degerini iki katına cıkarıp yazdırınız.

        System.out.println(sayi *=2);

        // sayıyı sadece bir arttırmak  veya azaltmak istiyorsanız;

        int sayi1= 10;
        sayi1++;//==>11
        sayi1--;//==9

    }
}
